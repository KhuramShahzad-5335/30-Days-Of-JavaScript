const A = 5;
const B = 10;

//using if else

if(A > B)
    console.log(`${A} is greater then ${B}`);
else
    console.log(`${B} is greater then ${A}`);

//using ternary operators

A > B
    ?   console.log(`${A} is greater then ${B}`)
    :   console.log(`${B} is greater then ${A}`);

