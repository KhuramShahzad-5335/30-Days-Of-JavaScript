//taking inputs
const length = parseFloat(prompt('Enter Length of Triange: ',''));
const width = parseFloat(prompt('Enter Width of Triange: ',''));

//calculating area.
const area = length * width;

// calculating permimeter.
const permimeter = 2 * (length + width)

//Showing the output
alert(`\nArea Of Rectangle is ${area}cm\n\n\nPerimeter of Rectangle is ${permimeter}cm`)

